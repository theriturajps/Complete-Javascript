// while

let i = 0

while(i <= 10){
    console.log(`Value of i is : ${i}`);
    i = i +2
}

console.log(`-----------------------------------------------------------`);

let myArray = ['flash', "batman", "superman"]
let arr = 0
while(arr < myArray.length){
    console.log(`Value is ${myArray[arr]}`);
    arr++
}

console.log(`------------------- Do While -------------------------------`);

let score = 0
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);

console.log(`-----------------------------------------------------------`);

